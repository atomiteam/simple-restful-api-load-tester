package com.atomiteam.load.model.executions;

import java.util.List;

public class Tests {

	List<Test> tests;
	Integer paralellism;
	Integer wait;

	public List<Test> getTests() {
		return tests;
	}

	public void setTests(List<Test> tests) {
		this.tests = tests;
	}

	public Integer getParalellism() {
		return paralellism;
	}

	public void setParalellism(Integer paralellism) {
		this.paralellism = paralellism;
	}

	public Integer getWait() {
		return wait;
	}

	public void setWait(Integer wait) {
		this.wait = wait;
	}

	
	
}
